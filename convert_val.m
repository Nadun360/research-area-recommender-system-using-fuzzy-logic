function val = convert_val(val_str)
    switch(val_str)
        case 'b1'
            val = 10;
        case 'b2'
            val = 20;
        case 'b3'
            val = 30;
        case 'b4'
            val = 40;
        case 'b5'
            val = 50;
        case 'b6'
            val = 60;
        case 'b7'
            val = 70;
        case 'b8'
            val = 80;
        case 'b9'
            val = 90;
        case 'b10'
            val = 100;
    end
end